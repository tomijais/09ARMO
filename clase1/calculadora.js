let calculadora = {
    sumar: function(numeroA, numeroB){
        return numeroA + numeroB
    },
    restar: function(numeroA, numeroB){
        return numeroA - numeroB
    },
    multiplicar: function(numeroA, numeroB){
        return numeroA * numeroB
    },
    dividir: function(numeroA, numeroB){
        return numeroA / numeroB
    },
    
}

module.exports = calculadora;
